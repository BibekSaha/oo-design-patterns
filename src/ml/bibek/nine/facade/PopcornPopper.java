package ml.bibek.nine.facade;

public class PopcornPopper {
    public void on() {
        System.out.println("The Popcorn Popper is turned on");
    }

    public void pop() {
        System.out.println("The Popcorn Popper is ready to pop...");
    }

    public void off() {
        System.out.println("The Popcorn Popper is turned off");
    }
}
